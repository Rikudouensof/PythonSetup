
import math as ma;
pi = 3.142;
x= 10;
y = 12;
z = 30;


#round to the nearest whole number
print(round(pi));


#round a number up
print(ma.ceil(pi))


#round a number down
print(ma.floor(pi));


#get absolute number
print(abs(pi))


#Get power up(by 2)
print(pow(pi,2));


#Get the squareroot
print(ma.sqrt(pi));


#get the max number of numbers
print(max(x,y,z,pi));

#get the minimum number of numbers
print(min(x,y,z,pi));


